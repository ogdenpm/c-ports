bool EnterBlk() {
    if (blkSP < 20) {
        blkSP++;
        return true;
    }
    if (blkOverCnt == 0) {
        Tx2SyntaxError(ERR204); /*  LIMIT EXCEEDED: NUMBER OF ACTIVE */
                                /*  PROCEDURES and do CASE GROUPS */
        EmitTopItem();
    }
    blkOverCnt++;
    return false;
}

bool ExitBlk() {
    if (blkOverCnt > 0) {
        blkOverCnt--;
        return false;
    } else if (blkSP > 0) {
        blkSP--;
        return true;
    } else {
        Tx2SyntaxError(ERR205); /*  ILLEGAL NESTING OF BLOCKS, ENDS not BALANCED */
        EmitTopItem();
        return false;
    }
}

